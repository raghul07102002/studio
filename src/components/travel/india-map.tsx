interface IndiaMapProps {
    visitedStates: string[];
    onStateClick: (stateId: string) => void;
}

export function IndiaMap({ visitedStates, onStateClick }: IndiaMapProps) {

    return (
        <TooltipProvider>
            <svg
                viewBox="0 0 600 600"
                className="w-full h-full stroke-primary"
            >
                <g>
                    {statePaths.map((state) => {
                        const isVisited = visitedStates.includes(state.id);
                        return (
                            <Tooltip key={state.id}>
                                <TooltipTrigger asChild>
                                    <path
                                        d={state.d}
                                        onClick={() => onStateClick(state.id)}
                                        className={cn(
                                            "transition-all duration-300 cursor-pointer",
                                            isVisited 
                                                ? "fill-primary/80 stroke-primary-foreground/50" 
                                                : "fill-primary/10 hover:fill-primary/40 focus:outline-none"
                                        )}
                                    />
                                </TooltipTrigger>
                                <TooltipContent>
                                    <p>{state.name}</p>
                                </TooltipContent>
                            </Tooltip>
                        )
                    })}
                </g>
            </svg>
        </TooltipProvider>
    );
}
